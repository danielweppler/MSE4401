function j = jacobianDW(angle, l2, l3)

syms z1 o1 z2 o2 a1 t1 t2 t3

[a1,t1, t2,t3] = forKinDW(angle, l2, l3);

o0=[0; 0; 0;];
z0=([0; 0; 1;]);

z1 = [t1(1,3); t1(2,3); t1(3,3);];
o1 = [t1(1,4); t1(2,4); t1(3,4);];

t20=t1*t2;

z2 = [t20(1,3); t20(2,3); t20(3,3);];
o2 = [t20(1,4); t20(2,4); t20(3,4);];

z3 = [a1(1,3); a1(2,3); a1(3,3);];
o3 = ([a1(1,4); a1(2,4); a1(3,4);]);

j = [cross(z0, o3), z1, z2;...
    z0, o0, o0];
end